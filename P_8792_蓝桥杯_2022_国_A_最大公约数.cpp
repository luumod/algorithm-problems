const int N=1e5+10;
int n,a[N],step=1e9,live;
inline int gcd(int a,int b) 
{    
    if(b) while((a%=b) && (b%=a));    
    return a+b;
}
signed main(){
    std::cin>>n;
    for (int i=1;i<=n;i++){
        std::cin>>a[i];
        live+=(a[i]==1)?1:0;
    }
    if (live){ //原始存在1
        std::cout<<n-1;
        return 0;
    }
    int ans=1e9;
    for (int i=1;i<=n;i++){
        int x=a[i];
        for (int j=i+1;j<=n;j++){
            x=gcd(a[j],x);
            if (j-i>step) break;
            if (x==1){
                step=j-i;
                break;
            }
        }
    }
    if (step==1e9){
        std::cout<<-1;
    }
    else{
        std::cout<<n-1+step;
    }
    return 0;
}